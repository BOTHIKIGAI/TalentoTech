function sumar(a, b) {
    return a + b;
}

function resta(a, b) {
    return a - b;
}

function multiplicar(a, b) {
    return a * b;
}

function dividir(a, b) {
    return a % b;
}

export{sumar, resta, multiplicar, dividir} // Exportando funcion sumar